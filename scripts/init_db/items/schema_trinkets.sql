-- ================================================================================================
-- TRINKET CATEGORIES - Categorias de trinkets por livro e região
-- ================================================================================================

CREATE TABLE IF NOT EXISTS item_trinket_categories (
    trinket_category_id INTEGER PRIMARY KEY,
    book_id INTEGER NOT NULL,
    category_name TEXT NOT NULL UNIQUE,
    category_description TEXT,
    dice_type TEXT NOT NULL, -- 'd100', 'd8', etc.
    
    FOREIGN KEY (book_id) REFERENCES core_books(book_id)
);

-- ================================================================================================
-- TRINKETS - Trinkets individuais (ESTRUTURA OTIMIZADA)
-- ================================================================================================

CREATE TABLE IF NOT EXISTS item_trinkets (
    trinket_id INTEGER PRIMARY KEY AUTOINCREMENT,
    trinket_category_id INTEGER NOT NULL,
    dice_min INTEGER NOT NULL,     -- Valor mínimo do dado (ex: 1 em "1-2")
    dice_max INTEGER NOT NULL,     -- Valor máximo do dado (ex: 2 em "1-2")
    description TEXT NOT NULL,     -- Apenas descrição (sem nome redundante)
    
    FOREIGN KEY (trinket_category_id) REFERENCES item_trinket_categories(trinket_category_id)
);

-- ================================================================================================
-- ÍNDICES PARA PERFORMANCE
-- ================================================================================================

CREATE INDEX IF NOT EXISTS idx_trinkets_category ON item_trinkets(trinket_category_id);
CREATE INDEX IF NOT EXISTS idx_trinkets_dice_range ON item_trinkets(trinket_category_id, dice_min, dice_max);

