-- ================================================================================================
-- Esta view facilita a busca de trinkets baseada em rolagem de dados
-- Uso: SELECT * FROM trinket_search WHERE category_id = 1 AND 42 BETWEEN dice_min AND dice_max;

CREATE VIEW IF NOT EXISTS trinket_search AS
SELECT 
    t.trinket_id,
    t.trinket_category_id as category_id,
    tc.category_name,
    tc.dice_type,
    t.dice_min,
    t.dice_max,
    t.description,
    cb.name as book_name
FROM item_trinkets t
JOIN item_trinket_categories tc ON t.trinket_category_id = tc.trinket_category_id
JOIN core_books cb ON tc.book_id = cb.book_id;

